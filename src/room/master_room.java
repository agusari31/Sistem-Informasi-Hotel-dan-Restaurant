    private void tbl_roomDetailMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_roomDetailMouseClicked
        // TODO add your handling code here:
        String id = tbl_roomDetail.getValueAt(tbl_roomDetail.getSelectedRow(), 0).toString();
        String jenis = tbl_roomDetail.getValueAt(tbl_roomDetail.getSelectedRow(), 1).toString();
        String harga = tbl_roomDetail.getValueAt(tbl_roomDetail.getSelectedRow(), 2).toString();
        String status = tbl_roomDetail.getValueAt(tbl_roomDetail.getSelectedRow(), 3).toString();
        
        tf_id.setText(id);
        tf_jenis.setText(jenis);
        tf_harga.setText(harga);
        
        if(status.equals("Ready")){
            rd_ready.setSelected(true);
            rd_booked.setSelected(false);
        }else if(status.equals("Booked")){
            rd_ready.setSelected(false);
            rd_booked.setSelected(true);
        }
    }//GEN-LAST:event_tbl_roomDetailMouseClicked


