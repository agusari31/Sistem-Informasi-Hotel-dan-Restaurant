    private void btn_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahActionPerformed
        // TODO add your handling code here:
        String jenis = tf_jenis.getText().toString();
        String harga = tf_harga.getText().toString();
        String status = "";
        
        if(rd_ready.isSelected()){
            rd_ready.setSelected(true);
            rd_booked.setSelected(false);
            status = rd_ready.getText().toString();
        }else if(rd_booked.isSelected()){
            rd_ready.setSelected(false);
            rd_booked.setSelected(true);
            status = rd_booked.getText().toString();
        }
        
        String query = "INSERT INTO tb_room (jenis_kamar, harga, status) VALUES ('"
                + jenis+"',"+harga+",'"+status+"')";
        
        try{
            kn.getStatement().executeUpdate(query);
            JOptionPane.showMessageDialog(this, "Data berhasil Ditambahkan");
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Data gagal ditambahkan");
        }
        setDataToTabel();
        
       tf_id.setText("");
        tf_jenis.setText("");
        tf_harga.setText("");
        group_rd.clearSelection();
    }//GEN-LAST:event_btn_tambahActionPerformed

